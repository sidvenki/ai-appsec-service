# ── VULNERABILITY 6: Unbounded loop around LLM call (LLM10) ────────────────
@app.post("/api/refine")
async def refine_response(request: Request):
    """Refines a response in an unbounded loop."""
    body = await request.json()
    query = body.get("query", "")

    # VULN: while True loop with LLM call, no max iterations
    answer = ""
    while True:
        response = client.create(
            messages=[
                {"role": "user", "content": f"Improve this answer: {answer or query}"}
            ],
        )
        answer = response["content"]
        if "perfect" in answer.lower():
            break

    return {"response": answer}


# ── VULNERABILITY 7: Traditional SQL injection ──────────────────────────────
@app.get("/api/users")
async def get_users(name: str = ""):
    """SQL injection vulnerability."""
    conn = sqlite3.connect(":memory:")
    cursor = conn.cursor()
    cursor.execute("CREATE TABLE IF NOT EXISTS users (id INTEGER, name TEXT, email TEXT)")

    # VULN: SQL injection
    query = f"SELECT * FROM users WHERE name = '{name}'"
    cursor.execute(query)
    results = cursor.fetchall()
    conn.close()
    return {"users": results}


# ── VULNERABILITY 8: Command injection via subprocess ───────────────────────
@app.get("/api/ping")
async def ping_host(host: str = "localhost"):
    """Command injection vulnerability."""
    # VULN: User input in subprocess
    result = subprocess.run(f"ping -c 1 {host}", shell=True, capture_output=True, text=True)
    return {"output": result.stdout}


